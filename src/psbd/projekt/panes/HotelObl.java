    void renderuj() {
        int size = pokoje.size();
        p.setLayout(new GridLayout(size, 1));
        for (int i = 0; i < size; ++i) {
            oblozenieInt += pokoje.get(i).getOblozenie();
            p.add(pokoje.get(i));
        }
        lblObl.setText("ObloÅ¼enie: " + oblozenieInt);
        jScrollPane1.setViewportView(p);
        jScrollPane1.validate();
        jScrollPane1.repaint();
    }
